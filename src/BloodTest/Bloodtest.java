    private void BTN_ADDActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BTN_ADDActionPerformed
        String id=TXT_ID.getText();
        String alt_sgpt=TXT_ALT_SGPT.getText();
        String gamma=TXT_GAMMA.getText();
        String mcv=TXT_MCV.getText();
        String time=TXT_TIME.getText();
        DBHelpBloodTest db= new DBHelpBloodTest();
        DBHelpState db2= new DBHelpState();
        if("t0".equals(time)){
            db.addBloodTest(new BloodTestDB(id, alt_sgpt, gamma, mcv));
            db2.updateStateBlood(new state(id));
        }
        if("t6".equals(time)){
            db.addBloodTest_T6(new BloodTestDB(id, alt_sgpt, gamma, mcv));
            db2.updateStateBlood_6(new state(id));
        }
        if("t12".equals(time)){
            db.addBloodTest_T12(new BloodTestDB(id, alt_sgpt, gamma, mcv));
            db2.updateStateBlood_12(new state(id));
        }
        dispose();
    }//GEN-LAST:event_BTN_ADDActionPerformed

